package com.example.firsttestproject

fun main() {
    // Declare numbers
    val num1: Double = 10.6
    val num2: Double = 5.0

    // Perform arithmetic operations
    val sum = num1 + num2
    println("num1 + num2 = $sum")

    val difference = num1 - num2
    println("num1 - num2 = $difference")

    val product = num1 * num2
    println("num1 * num2 = $product")

    val quotient = num1 / num2
    println("num1 / num2 = $quotient")

    val remainder = num1 % num2
    println("num1 % num2 = $remainder")
}
